                $(this).parent().addClass("wait");
                $(this).parent().find(".wait").animate({width:129},200);
                setTimeout(function(){
                $.ajax({
                    url : 'addwishlist?idproduct=7', 
                    type : 'GET' ,
                    success: function(data) {
