    <p class="pro_title">NOIR LAMBSKIN <br/>刻黑牛肉</p>
    <p class="text-material">小牛皮是制作皮鞋和皮包的最高贵材料。其致密的质地为其带来了结构和形状。磨砂表面处理带来了莫列效果，增加了质感和深度。要创造这些表面效果，要将皮革进行不同程度打磨。在这种情况下，皮革表面被精心切割成爬行动物鳞片的造型。</p>
