        <p class="strong">1. 品牌简介</p>
        <p>1998年3月，Jérôme Dreyfuss创建了他的同名品牌，让当时年仅23岁的他特立独行而名声鹊起。</p>
        <p>Jérôme凭借他的成衣系列“Couture à Porter”声名大噪。该系列首推之际就获得极大的成功，人们也因此称他为“法国时尚顽童”。活跃了四年之后，经过深思熟虑，Jérôme于2002年选择致力于创作具有传统奢侈品风格的皮革制品。<br/>
        Jérôme的灵感来源于通过观察自己的妻子和朋友，以及巴黎后街的女性们，并尝试用心去感受这些人们的真正需求。于是，他决心设计一系列美观且实用，拥有内涵且一眼就能认出的高辨识度包袋来吸引女性的购买欲。<br/>
        这个聪明而独特的男人，为每个包赋予了一个男性的名字，既显示他的自身个性又彰显了品牌的友善。“我是一个在办公室里爱开玩笑的人。” Jérôme调笑道“我喜欢大笑，给每个包命名升华了我的幽默。”毕竟，谁能抵抗的了“Billy”的魅力？除了“Robert”，亦或是难以驾驭的“Momo”…？<br/> 
        Jérôme热衷于30年代和60年代的建筑风格，JD从建筑的实用性和美学中汲取灵感。他的创作过程往往通过将分割内容再创作，以适应周边的需求。   
        </p>
        <p class="strong">2. 品牌精髓</p>
        <p>凭着大胆，极致柔性材质，与众不同的雅致，热情而传统的工艺技术以及对环境的尊重，这些方式成功塑造和构架了JD品牌精髓。1998年，ANDAM时尚大奖得主。1998年，“Venus de la Mode”年度时尚设计师得主。2009年，JD进军皮夹克市场，重新启用署名功能。2012年，JD进军男包市场，推出一系列实用性包款。2013年，JD设计并推出了第一批鞋履系列，充满了女性化的代码。
        </p>
        <p class="strong">3. 怎样界定JD包袋的特性</p>
        <p class="strong">一个丰富的创意：</p>
        <div class="line_h">
            <li>柔性包袋的先驱，拥有“奢华的根基”以及明亮的色彩选择。</li>
            <li>每一季都有新品指南，100%的革新材料延续着每一个系列，其中包括标志性的“timeless”系列等。</li>
            <li>JD的标识有很高的辨识度，统一而严谨。</li>
