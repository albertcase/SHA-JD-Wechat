        <p class="strong">4. 确保配饰的质量</p>
        <p>我们高度重视产品的质量问题，并在每个阶段的生产过程中都研发了特定的质量控制程序。精挑细选的供应商：JD团队挑选了唯一的一小部分欧洲供应商，他们在行业内经验十足，品质牢靠，供应链稳定。监管部门严格把控皮革质量： 每一个新品都会有严格的检控，基于我们的高标准，有时候甚至会淘汰30%的皮革。剪裁：每一块包袋上的皮革都由工匠再次细心检测，假如有监管层发现那些存有瑕疵的皮革，工匠们可以杜绝这一情况的发生。仔细包装，但是拒绝浪费。当包袋到达店铺的时候，将再次对包身做一次全面检查。</p>
        <p>在大货生产的前几周，需要给模型分次序的做大量测试，例如强度，天气承受力，和其他关键的产品特性等。</p>
        <p>鞋履也会施加同样的程序。</p>  

